_current_lang = 'ru'

def set_language(lang: str):
    global _current_lang
    if lang in _strings:
        _current_lang = lang
    else:
        raise ValueError(f"Unsupported language: {lang}. Use 'ru' or 'en'.")

def get(key: str, *args, **kwargs) -> str:
    text = _strings[_current_lang].get(key, key)
    if args:
        return text.format(*args)
    if kwargs:
        return text.format(**kwargs)
    return text